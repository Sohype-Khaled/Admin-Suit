$(document)
    .on('click', '.admin-suit-action', function () {
        var data = $(this)
        $('#admin-suit-action-modal_title').text(data.data('title'))
        $('#admin-suit-action-modal_message').text(data.data('message'))
        $('#admin-suit-action-modal_model').val(data.data('model'))
        $('#admin-suit-action-modal_action').val(data.data('action'))
        $('#admin-suit-action-modal_items').val(data.data('items'))
        $('#admin-suit-action-modal_value').val(data.data('value'))
    })
    .on('click', '.admin-suit-bulk-action', function () {
        var data = $(this)
        $('#admin-suit-action-modal_title').text(data.data('title'))
        $('#admin-suit-action-modal_message').text(data.data('message'))
        $('#admin-suit-action-modal_model').val(data.data('model'))
        $('#admin-suit-action-modal_action').val(data.data('action'))
        $('#admin-suit-action-modal_value').val(data.data('value'))
    })
    .on('hide.bs.modal', '#admin-suit-action-modal', function () {
        $('#admin-suit-action-modal_title').text('')
        $('#admin-suit-action-modal_message').text('')
        $('#admin-suit-action-modal_model').val('')
        $('#admin-suit-action-modal_action').val('')
        $('#admin-suit-action-modal_items').val('')
    })